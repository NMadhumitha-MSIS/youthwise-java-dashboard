    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
         String userName = txtUsernamee.getText();
        //String password = txtPass.getPassword();
        char[] passwordCharArray = txtPass.getPassword();
        String password = String.valueOf(passwordCharArray);
        Organization organization = (Organization) cmbOrg.getSelectedItem();
        Employee employee = (Employee) cmbEmp.getSelectedItem();
        Role role = (Role) cmbRolebox.getSelectedItem();

        if(userName.equals("") || password.equals("")){
            JOptionPane.showMessageDialog(null, "Please agree to the terms of service", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if(organization.getUserAccountDirectory().checkIfUsernameExists(userName)){
            JOptionPane.showMessageDialog(null, "Username already exists, select a new username", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        organization.getUserAccountDirectory().createUserAccount(userName, password, employee, role);
        //dB4OUtil.storeSystem(system);
        txtUsernamee.setText("");
        txtPass.setText("");
        populateData();
    }//GEN-LAST:event_btnCreateActionPerformed


