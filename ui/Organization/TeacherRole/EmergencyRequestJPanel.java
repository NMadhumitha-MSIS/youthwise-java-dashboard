    public void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) tblChatHistory.getModel();
       // System.out.println("Hello inside table" + sender.getWorkQueue().getWorkRequestTeacherList());
        model.setRowCount(0);
        for (WorkRequest request : sender.getWorkQueue().getWorkRequestTeacherList()) {
            System.out.println("Hello from table" + request.getMessage());
            Object[] row = new Object[4];
            row[0] = request.getMessage();
            row[1] = request.getReceiver();
            row[2] = request.getStatus();
            String result = ((EmergencyWorkRequest) request).getResultMessage();
            row[3] = result == null ? "Waiting" : result;

            model.addRow(row);
        }
    }
    // Populate students dynamically from the system

    private void populateStudentComboBox() {
        cmbStudents.removeAllItems();
        Organization studentOrg = system.getOrganizationDirectory().findOrganization(Organization.Type.Student);
        if (studentOrg != null) {
            StudentDirectory studentDirectory = studentOrg.getStudentDirectory();
            if (studentDirectory != null) {
                for (Student student : studentDirectory.getStudentList()) {
                    cmbStudents.addItem(student);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "No students found in the system.");
        }
    }


