    private void btnAlertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlertActionPerformed
        // TODO add your handling code here:
        Student selectedStudent = (Student) cmbStudents.getSelectedItem();
        String reqMessage = txtareaMsg.getText();

        if (reqMessage.equals("") || reqMessage.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a message to send.");
            return;
        }

        // Check if a student is selected and message is provided
        if (selectedStudent == null) {
            JOptionPane.showMessageDialog(null, "Please select a student and enter a message.");
            return;
        }

        EmergencyWorkRequest message = new EmergencyWorkRequest();
        message.setMessage(reqMessage);
        message.setSender(sender);
        // message.setReceiver("");
        message.setStatus("Sent");
        // Use the `selectedTeacher` object to send your message
//        message.setReceiver(cmbTeacher.getSelectedItem());
        txtStatus.setText("Sent");

        Organization orgn = null;
        for (EnterpriseNetwork network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if (organization instanceof MedicalDoctorOrganization) {
                        orgn = organization;
                        break;
                    }
                }
            }
        }

        System.out.println("Whats the organization " + orgn);
        if (orgn != null) {
            orgn.getWorkQueue().getEmergencyWorkRequest().add(message);
            System.out.println("inside send button " + orgn.getWorkQueue().getEmergencyWorkRequest().get(0));
            sender.getWorkQueue().getWorkRequestTeacherList().add(message);
        }

        JOptionPane.showMessageDialog(null, "Request message sent", "Information", JOptionPane.INFORMATION_MESSAGE);
        populateRequestTable();
    }//GEN-LAST:event_btnAlertActionPerformed

