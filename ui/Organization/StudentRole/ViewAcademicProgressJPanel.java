    private void populateStudentMarksTable() {
        DefaultTableModel model = (DefaultTableModel) tblMarks.getModel();
        model.setRowCount(0);

        // Check if the account is of Student
        if (!(account.getRole() instanceof StudentRole)) {
            JOptionPane.showMessageDialog(null, "Logged-in user is not assigned the StudentRole!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Student student = account.getStudent(); 
        if (student == null) {
            JOptionPane.showMessageDialog(null, "Student details not found for the logged-in user!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        for (String testName : student.getTestMarks().keySet()) {
            Map<String, Integer> marks = student.getMarksForTest(testName);

            int englishMarks = marks.getOrDefault("English", 0);
            int mathsMarks = marks.getOrDefault("Maths", 0);
            int frenchMarks = marks.getOrDefault("French", 0);
            int socialMarks = marks.getOrDefault("Social", 0);
            int scienceMarks = marks.getOrDefault("Science", 0);

            Object[] row = new Object[]{
                testName, 
                englishMarks, 
                mathsMarks, 
                frenchMarks, 
                socialMarks,
                scienceMarks 
            };
            model.addRow(row);
        }
        // Get total marks
        double totalMarks = student.getTotalMarks();
        txtTotal.setText(String.valueOf(totalMarks));
        System.out.println("Total Marks: " + totalMarks);
 
        // Get grade
        String grade = student.getGrade_Score();
        System.out.println("Grade: " + grade);
        txtGrade.setText(grade);
 
        // Get CGPA
        double cgpa = student.getCGPA();
        System.out.println("CGPA: " + cgpa);
        txtgpa.setText(String.valueOf(cgpa));
    }


