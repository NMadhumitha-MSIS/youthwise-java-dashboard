    private void btnAssignSelfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignSelfActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblWorkRequest.getSelectedRow();

        if (selectedRow >= 0) {
            WorkRequest request = (WorkRequest) tblWorkRequest.getValueAt(selectedRow, 0);
            if (request.getMessage().equalsIgnoreCase("Completed")) {
                JOptionPane.showMessageDialog(null, "Request already acknowledged and completed.");
                return;
            } else {
                request.setReceiver(sender);
                request.setStatus("Assigned");

                populateTable();

            }

        } else {
            JOptionPane.showMessageDialog(null, "Choose a reuest to acknowledge.");
            return;
        }
    }//GEN-LAST:event_btnAssignSelfActionPerformed

    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblWorkRequest.getModel();

        model.setRowCount(0);
        for (WorkRequest request : organization.getWorkQueue().getEmergencyWorkRequest()){
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = request.getSender().getEmployee() == null ? null : request.getSender().getEmployee().getName();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[3] = request.getStatus();

            model.addRow(row);
        }
    }
    
