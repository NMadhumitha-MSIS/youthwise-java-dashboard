    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:  
         System.out.println("Inside acknowledge");
        int selectedRow = tblWorkRequest.getSelectedRow();
        if (selectedRow >= 0) {
            emergencyWorkRequest = (EmergencyWorkRequest) tblWorkRequest.getValueAt(selectedRow, 0);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a request message to process.");
            return;
        }
        
        if (txtDoctorsComments.getText().equals("") || txtDoctorsComments.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a resolution to send.");
            return;
        }
        
        
        emergencyWorkRequest.setResultMessage(txtDoctorsComments.getText());
        emergencyWorkRequest.setStatus("Resolved");
        populateTable();
        JOptionPane.showMessageDialog(null, "Message processed");

    }//GEN-LAST:event_btnSaveActionPerformed

