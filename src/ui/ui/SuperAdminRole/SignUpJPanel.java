    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();

        String email = txtEmail.getText();
        String username = txtUserName.getText();
        String password = String.valueOf(txtPwd.getPassword());

        String phno = txtPNo.getText();
        String ParentOf = txtPO.getText();

        if (name.equals("") || email.equals("") || username.equals("") || password.equals("") || phno.equals("") || ParentOf.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter all Values", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!txtName.getText().matches("^[A-Za-z]+$")) {
            JOptionPane.showMessageDialog(this, "Invalid Name");
            return;
        }

        if (!txtEmail.getText().matches("[a-zA-Z0-9]+@(gmail|yahoo|[^.]+\\.com)")) {
            JOptionPane.showMessageDialog(this, "Please enter a correct email id!");
            return;

        }

        if (system.getUserAccountDirectory().checkIfUsernameExists(username)) {
            JOptionPane.showMessageDialog(null, "Username already exists, select a new username", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Organization organization = system.getOrganizationDirectory().createOrganization(Organization.Type.Parent, "Parent");
        ParentDirectory parentdirectory = system.getParentdirectory();
        Parent parent = parentdirectory.createParent(name,email,ParentOf,phno);
       
        UserAccount account = system.getUserAccountDirectory().createUserAccount(username, password, new ParentRole());
        account.setParent(parent);
        JOptionPane.showMessageDialog(this, "Parent Account Created!"); 
        
        txtName.setText("");
        txtEmail.setText("");
        txtPwd.setText("");
        txtUserName.setText("");
        txtPNo.setText("");
        txtPO.setText("");

    }//GEN-LAST:event_btnSignUpActionPerformed

